<script context="module" lang="ts">
	import {
		InsertStructureDocument,
		InsertStructureMutation,
		InsertStructureMutationVariables,
		UpdateStructureDocument,
		UpdateStructureMutation,
		UpdateStructureMutationVariables,
	} from '$lib/graphql/_gen/typed-document-nodes';
	import type { Structure, StructureRequest, InputItem } from '$lib/types';
	import { Alert, Button, Input } from '$lib/ui/base';
	import { default as deepEqual } from 'fast-deep-equal';
	import { createEventDispatcher } from 'svelte';
	import { operationStore, mutation } from '@urql/svelte';
	import type { OperationStore } from '@urql/svelte';
	import * as RD from '$lib/remoteData';
</script>

<script lang="ts">
	export let structure: StructureRequest;
	export let fieldErrors: StructureRequest;
	export let confirmText = 'Confirmer';
	export let onCancel: () => void | null = null;
	export let disabledKeys: Record<InputItem['key'], boolean> = {};
	export let structureId: string | null = null;

	let originalStructure = { ...structure };

	$: untouched = deepEqual(structure, originalStructure);
	$: isValid = validateStructure(structure);

	const dispatch = createEventDispatcher();

	function validateStructure({ name }: StructureRequest) {
		return Boolean(name);
	}

	const updateStore: OperationStore<
		UpdateStructureMutation,
		UpdateStructureMutationVariables,
		Structure
	> = operationStore(UpdateStructureDocument);
	const insertStore: OperationStore<
		InsertStructureMutation,
		InsertStructureMutationVariables,
		Structure
	> = operationStore(InsertStructureDocument);
	const updater = mutation(updateStore);
	const inserter = mutation(insertStore);
	let mutationResult: RD.RemoteData<Structure, string> = RD.notAsked;

	insertStore.subscribe((result) => {
		if (result.data) {
			mutationResult = RD.success(result.data);
		}
		if (result.error) {
			const err = result.error.toString();
			mutationResult = RD.failure(err);
		}
	});
	updateStore.subscribe((result) => {
		if (result.data) {
			mutationResult = RD.success(result.data);
		}
		if (result.error) {
			const err = result.error.toString();
			mutationResult = RD.failure(err);
		}
	});

	function onInput() {
		mutationResult = RD.notAsked;
	}

	async function handleSubmit() {
		mutationResult = RD.loading;
		if (structureId) {
			updater({ ...structure, id: structureId });
		} else {
			inserter({ ...structure });
		}
		dispatch('submit', { structure });
	}

	async function handleCancel() {
		if (onCancel) {
			onCancel();
		}
		dispatch('cancel', {});
	}
</script>

<div class="w-full">
	{#if RD.getData(mutationResult)}
		<div role="status">
			<h2 class="text-france-blue fr-h4 pt-8 px-8">
				{structureId ? 'Modification' : 'Création'} d'une structure
			</h2>
			<div class="w-full p-8">
				<Alert type="success" description={'La structure a été enregistrée avec succès !'} />
			</div>
			<div class="w-full px-8">
				<Button on:click={handleCancel}>J'ai compris</Button>
			</div>
		</div>
	{:else}
		<h2 class="text-france-blue fr-h4 pt-8 px-8">
			{structureId ? 'Modification' : 'Création'} d'une structure
		</h2>
		<form class="w-full px-8 pb-8" on:submit|preventDefault={handleSubmit}>
			<Input
				bind:val={structure['name']}
				inputHint={'Ex : Mission locale de Crest'}
				inputLabel={'Nom'}
				error={fieldErrors['name']}
				on:input={onInput}
				disabled={disabledKeys['name']}
				required={true}
			/>
			<Input
				bind:val={structure['phone']}
				inputHint={'Ex : 04 75 76 70 67'}
				inputLabel={'Téléphone'}
				error={fieldErrors['phone']}
				on:input={onInput}
				disabled={disabledKeys['phone']}
				required={true}
			/>
			<Input
				bind:val={structure['email']}
				inputHint={'Ex : crest@mission-locale.fr'}
				inputLabel={'Courriel'}
				error={fieldErrors['email']}
				on:input={onInput}
				disabled={disabledKeys['email']}
				type={'email'}
				required={true}
			/>
			<Input
				bind:val={structure['address1']}
				inputHint={'Ex : 3 Rue des Cuiretteries'}
				inputLabel={'Adresse'}
				error={fieldErrors['address1']}
				on:input={onInput}
				disabled={disabledKeys['address1']}
				required={true}
			/>
			<Input
				bind:val={structure['address2']}
				inputHint={'Ex : Conseiller en réinsertion'}
				inputLabel={'Adresse (complément)'}
				error={fieldErrors['address2']}
				on:input={onInput}
				disabled={disabledKeys['address2']}
			/>
			<Input
				bind:val={structure['postalCode']}
				inputHint={'Ex : 26400'}
				inputLabel={'Code postal'}
				error={fieldErrors['postalCode']}
				on:input={onInput}
				disabled={disabledKeys['postalCode']}
			/>
			<Input
				bind:val={structure['city']}
				inputHint={'Ex : Crest'}
				inputLabel={'Ville'}
				error={fieldErrors['city']}
				on:input={onInput}
				disabled={disabledKeys['city']}
				required={true}
			/>
			<Input
				bind:val={structure['website']}
				inputHint={'Ex : https://www.mission-locale.fr/crest'}
				inputLabel={'Site internet'}
				error={fieldErrors['website']}
				on:input={onInput}
				disabled={disabledKeys['website']}
			/>
			<Input
				bind:val={structure['siret']}
				inputHint={'Ex : XXX XXX XXX XXXXX'}
				inputLabel={'Siret'}
				error={fieldErrors['siret']}
				on:input={onInput}
				disabled={disabledKeys['siret']}
			/>
			<Input
				bind:val={structure['shortDesc']}
				inputHint={'Ex : Antenne de Crest de la Mission locale Auvergne Rhône-Alpes'}
				inputLabel={'Description'}
				error={fieldErrors['shortDesc']}
				on:input={onInput}
				disabled={disabledKeys['shortDesc']}
			/>
			{#if RD.getError(mutationResult)}
				<div class="mb-8">
					<Alert type="error" description={RD.getError(mutationResult)} />
				</div>
			{/if}
			<div class="flex flex-row gap-6 mt-12">
				<Button type="submit" disabled={!isValid || untouched || RD.isLoading(mutationResult)}
					>{confirmText}</Button
				>
				<Button outline={true} on:click={handleCancel}>Annuler</Button>
			</div>
		</form>
	{/if}
</div>
